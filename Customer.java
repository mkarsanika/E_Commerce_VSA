    private void jTextField7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField7ActionPerformed
    	if(jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty()) 
    	{
    		JOptionPane.showMessageDialog(this,"Enter The Product Id  | Product Quantity");
    		 Clear_data(); 
    	} 
    	else 
    	{
    		try {
                Connection conn=Db.Connect();    
                String query=Query.Customer_search_product;
                PreparedStatement stm=conn.prepareStatement(query);
                stm.setInt(1,Integer.valueOf(jTextField4.getText()));
                ResultSet rs=stm.executeQuery();
                rs.next();
                String play=rs.getString(1);
                Quantity=rs.getInt(2);
                int Price=rs.getInt(3); 
                String Category_table=String.valueOf(jComboBox1.getSelectedItem());
                int Quantity_table=Integer.valueOf(jTextField5.getText());
      
                if(play.equals(Category_table))
                {
                     if(Quantity_table<=Quantity) 
                     {  
                    	 int Price_table=Price*Quantity_table;
                    	 jTextField7.setText(String.valueOf(Price_table)); 
                     }
                     else 
                     { 
                    	 JOptionPane.showMessageDialog(this,"Product Quantity is less");
                    	 Clear_data();
                     }
               	}   
    		    else 
    		    { 
    		    	JOptionPane.showMessageDialog(this,"Choice Valid Category Product");
    		    	 Clear_data();
    		    }
    		}
           catch (SQLException e) {
          	 JOptionPane.showMessageDialog(this,"Choice Valid Category Product"); 
          	 Clear_data();
           }      
    	}
   
    }//GEN-LAST:event_jTextField7ActionPerformed
    
