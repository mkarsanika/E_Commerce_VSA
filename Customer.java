	public void Update_product(Integer a,Integer b,Integer Id) 
	{ 
		try {
            Connection conn=Db.Connect();    
            String query=Query.Customer_product;
            PreparedStatement stm=conn.prepareStatement(query);
            stm.setInt(1,a-b);
            stm.setInt(2,Id);
            stm.execute();
            conn.close();     
    	}
       catch (SQLException e) {
      	 JOptionPane.showMessageDialog(this,"No Information" +e.getMessage()); 	 
       } 
	}
	private void insert_data() {
		// TODO Auto-generated method stub
    	try {
            Connection conn=Db.Connect();    
            String query=Query.Customer_add_to_cart;
            PreparedStatement stm=conn.prepareStatement(query);
            stm.setInt(1,Integer.valueOf(jTextField4.getText()));
            stm.setInt(2,Integer.valueOf(jTextField5.getText()));
            stm.setInt(3,Integer.valueOf(jTextField7.getText()));
            stm.setString(4,jTextField1.getText());
            stm.execute();
            JOptionPane.showMessageDialog(this,"Product Added to Cart");
            Update_product(Quantity,Integer.valueOf(jTextField5.getText()),Integer.valueOf(jTextField4.getText()));
            conn.close();     
    	}
       catch (SQLException e) {
      	 JOptionPane.showMessageDialog(this,"You Already Added This Product"); 	 
       } 
	}

